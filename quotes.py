def normalize_author(author: str) -> str:
    """
    Yazar adını temizler. Şu an 'atfedilir' vb. ekler zaten kullanılmıyor ama
    yapı ileride ihtiyaç olursa diye duruyor.
    """
    if not author:
        return ""
    return author.strip()
